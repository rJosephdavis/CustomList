namespace CustomListProject
{
    class Generic_Comparer<T>
    {
        
        public bool Comparer(T firstValue, T secondValue)
        {
            if (firstValue.Equals(secondValue))
            {
                return true;
            }
            else
            {
                return false;
            }
        public bool Comparer(int firstValue, int secondValue)
            {
                if (firstValue.Equals(secondValue))
                {
                    return true;
                }
                else
                {
                    return false;
                }

        public bool Comparer (string firstValue, string secondValue))
                {
                    if (firstValue.Equals(secondValue))
                    {
                        return true;
                    }
                    else
                    {
                        return false;
                    }















                }














            }
